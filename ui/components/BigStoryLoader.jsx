import React, { Component } from 'react';

export default class BigStoryLoader extends Component {
  constructor(props){
    super(props)
    this.state={
      leftpos: 10, 
      topPos:40,
      scale:1,
      eatenDonut: false,
      eatenPineapple: false,
      eatenMarmelade:false,
    }
  }

  componentWillMount() {
    setTimeout(()=>{
      this.setState({
       leftpos: this.state.leftpos + 20,
       topPos: this.state.topPos+15,
    })
    })
    
  
    this.interval = window.setInterval(() => {
      for(let x=0;x<4;x++){
      if(this.state.leftpos===x*20+10){
        this.setState({
          scale: this.state.scale+ 0.2,
          leftpos: this.state.leftpos + 20,
          topPos: this.state.topPos+15
        })
        switch(x){
            case 1:
              this.setState({
                eatenDonut:true
               })
               break
             case 2:
              this.setState({
                eatenPineapple:true
               })
               break
              case 3:
              this.setState({
                eatenMarmelade:true
               })
               break
         }
        
        break;
        
        }
      }
      
    }, 3300);
       

  }
  donut(){
    return this.state.eatenDonut ? <Crums/> : <Donut/>
  }
  pineapple(){
    return this.state.eatenPineapple ? <Crums/>: <Pineapple/>
  }
  marmelade(){
    return this.state.eatenMarmelade ? <Crums/>: <Marmelade/>
  }

  componentWillUnmount() {
    window.clearInterval(this.interval);
  }
  render(){
    return(<div className="big-story-loader">
        <div id='bunny-container' style={{left: this.state.leftpos+'vw',
           transform:'scale('+ this.state.scale+')'}}>
          <FatBunny />
        </div>
        {this.donut()}
        {this.pineapple()}
        {this.marmelade()}
        
        </div>
    )
  }  
}

